def get_schema(error_code: str) -> Optional[dict]:
    """Получить схему по коду ошибки."""
    return _SCHEMAS.get(error_code.upper())


def get_schema_or_upgrade(error_code: str, is_paid: bool = True) -> dict:
    """
    Получить схему (тестовая версия — всё бесплатно).
    """
    schema = get_schema(error_code)
    if schema:
        return {"available": True, "data": schema}
    else:
        return {
            "available": False,
            "message": f"Схема для кода {error_code} пока не добавлена в базу.",
        }


def list_available_schemas() -> list[dict]:
    """Список всех доступных схем."""
    return [
        {"code": code, "title": s["title"], "description": s["description"]}
        for code, s in _SCHEMAS.items()
    ]
